    const favoriteItemsElem = document.getElementById("favoriteItems");

    function getFavorites() {
      return JSON.parse(localStorage.getItem("favorites") || "[]");
    }
    function setFavorites(favorites) {
      localStorage.setItem("favorites", JSON.stringify(favorites));
    }

 
    function renderFavorites() {
      const favorites = getFavorites();
      favoriteItemsElem.innerHTML = "";

      if (favorites.length === 0) {
        favoriteItemsElem.innerHTML = "<p class='text-center text-gray-600'>Favorit siyahısı boşdur</p>";
        return;
      }

      favorites.forEach(id => {
        const product = pizzaData.find(p => p.id === id);
        if (!product) return;
        favoriteItemsElem.innerHTML += `
          <div class="bg-white rounded-md shadow-md p-4 flex items-center justify-between">
            <div class="flex items-center space-x-4">
              <img src="${product.img}" alt="${product.name}" class="w-20 h-20 object-cover rounded" />
              <div>
                <p class="font-semibold">${product.name}</p>
                <p>${product.price.toFixed(2)} AZN</p>
              </div>
            </div>
            <button onclick="removeFavorite('${id}')" class="text-red-600 hover:text-red-800 text-xl" title="Favoritdən sil">
              <i class="fas fa-trash"></i>
            </button>
          </div>
        `;
      });
    }

   
    function removeFavorite(id) {
      let favorites = getFavorites();
      favorites = favorites.filter(favId => favId !== id);
      setFavorites(favorites);
      renderFavorites();
    }

    renderFavorites();